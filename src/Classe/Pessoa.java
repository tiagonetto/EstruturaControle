public class Pessoa {
    
    private String nome;
    private int idade;
    private Integer peso;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    
    public Integer getPeso() {
        return peso;
    }

    
    public void setPeso(Integer peso) {
        this.peso = peso;
    }
    
    
    
}
